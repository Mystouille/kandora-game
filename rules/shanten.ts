/**
 * Shanten of a closed hand: minimum number of tile swaps (replacing
 * one tile with another) needed to reach tenpai (-1 means winning).
 *
 * Accepts either a tile-string array (any length 1..14) or an
 * already-built `HandCounts`. The function does not enforce a
 * particular tile count; callers can compute "what would my shanten
 * be if I drew this tile?" by appending and re-querying.
 */
export function shanten(input: readonly Tile[] | HandCounts): number {
  const counts = Array.isArray(input)
    ? countsFromTiles(input as readonly Tile[])
    : (input as HandCounts);
  return Math.min(
    standardShanten(counts),
    chiitoitsuShanten(counts),
    kokushiShanten(counts)
  );
}

/**
 * Tiles whose addition would strictly reduce shanten — i.e. the
 * "ukeire" / acceptance set for the hand. Includes red-five-aware
 * iteration: only the canonical 5 is tested per suit.
 */
export function acceptanceTiles(input: readonly Tile[] | HandCounts): Tile[] {
  const counts = Array.isArray(input)
    ? countsFromTiles(input as readonly Tile[])
    : ({
        m: [...(input as HandCounts).m],
        p: [...(input as HandCounts).p],
        s: [...(input as HandCounts).s],
        z: [...(input as HandCounts).z],
      } as HandCounts);
  const baseline = shanten(counts);
  const out: Tile[] = [];
  const suits: Array<"m" | "p" | "s"> = ["m", "p", "s"];
  for (const suit of suits) {
    for (let i = 0; i < 9; i++) {
      if (counts[suit][i] >= 4) {
        continue;
      }
      counts[suit][i]++;
      if (shanten(counts) < baseline) {
        out.push(`${i + 1}${suit}`);
      }
      counts[suit][i]--;
    }
  }
  for (let i = 0; i < 7; i++) {
    if (counts.z[i] >= 4) {
      continue;
    }
    counts.z[i]++;
    if (shanten(counts) < baseline) {
      out.push(`${i + 1}z`);
    }
    counts.z[i]--;
  }
  return out;
}

/** True iff the hand is tenpai (shanten === 0 with 13-tile hand). */
export function isTenpai(input: readonly Tile[] | HandCounts): boolean {
  return shanten(input) === 0;
}

/**
 * Wait tiles for a tenpai hand: the subset of acceptance tiles
 * that still has at least one unseen copy in the seat's own hand
 * — i.e. tiles where the seat does not already hold all four
 * copies (kara-ten wait).
 *
 * Returns `[]` for non-tenpai hands and for "no-wait tenpai"
 * shapes such as `123m123p234s9999s` where `shanten()` reports 0
 * but every wait copy is locked in the hand itself. A non-empty
 * result is the canonical "is tenpai for riichi / tenpai-payment
 * purposes" predicate.
 */
export function waits(input: readonly Tile[] | HandCounts): Tile[] {
  const counts = Array.isArray(input)
    ? countsFromTiles(input as readonly Tile[])
    : (input as HandCounts);
  if (shanten(counts) !== 0) {
    return [];
  }
  return acceptanceTiles(counts);
}

// ---------------------------------------------------------------------------
// Optional eager precompute
// ---------------------------------------------------------------------------

/**
 * Eagerly fill the per-suit cache for every reachable counts vector
 * up to a given total tile count. Useful when the caller wants the
 * lookup behavior at startup instead of warming on demand.
 *
 * Cost: ~5^9 / 2 reachable entries, ≤2 seconds on a modern laptop.
 */
export function precomputeSuitTable(maxTotal = 14): void {
  const c = new Array(9).fill(0);
  fill(c, 0, 0, maxTotal);
}

function fill(c: number[], i: number, sum: number, maxTotal: number): void {
  if (i === 9) {
    solveSuit(c);
    return;
  }
  for (let n = 0; n <= 4 && sum + n <= maxTotal; n++) {
    c[i] = n;
    fill(c, i + 1, sum + n, maxTotal);
  }
  c[i] = 0;
}

/**
 * Fast "is this a winning shape?" check for ron / tsumo gating.
 *
 * Returns `true` iff the concealed hand + already-declared melds +
 * the candidate `winTile` form a valid agari shape (standard 4
 * melds + 1 pair, chiitoitsu, or kokushi). This is a shape-only
 * check — yaku, dora, fu, riichi, etc. are not considered. Callers
 * still need to call `scoreHand` for the yaku gate, but can short-
 * circuit the expensive call when this returns `false`.
 *
 * Kans are normalized to triplets for shape purposes (one tile per
 * kan dropped from the counts) so the underlying algorithms see a
 * standard 14-tile picture.
 */
export function isWinningShape(
  hand: readonly Tile[],
  melds: readonly { tiles: readonly Tile[] }[],
  winTile: Tile
): boolean {
  const counts = countsFromTiles(hand);
  for (const m of melds) {
    // Add the meld tiles back, but cap kan contributions at 3 so
    // shape totals stay in the 14-tile budget.
    const limit = m.tiles.length === 4 ? 3 : m.tiles.length;
    for (let i = 0; i < limit; i++) {
      const { suit, index } = tileToIndex(m.tiles[i]);
      counts[suit][index]++;
    }
  }
  const wt = tileToIndex(winTile);
  counts[wt.suit][wt.index]++;
  if (standardShanten(counts) === -1) {
    return true;
  }
  // Chiitoitsu / kokushi require a fully concealed hand.
  if (melds.length === 0) {
    if (chiitoitsuShanten(counts) === -1) {
      return true;
    }
    if (kokushiShanten(counts) === -1) {
      return true;
    }
  }
  return false;
}
